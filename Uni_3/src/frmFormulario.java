    public void cargarDatos(Empleado emp){
        this.txtNombre.setText(emp.getNombre());
        this.txtApellido.setText(emp.getApellido());
        this.txtCorreo.setText(emp.getCorreo());
        this.txtCelular.setText(Integer.toString(emp.getCelular()));
        
    }
