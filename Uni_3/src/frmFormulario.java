    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //se crean objetos para tratar la infromacion o el dato de cada uno de los txt
        String a = txtNombre.getText();
        String b = txtApellido.getText();
        String c = txtCorreo.getText();
        String d = txtCelular.getText();
        
        int e =Integer.parseInt(d);
        //Se invoca a main class Datos
        Datos.guardar(a,b,c,e);
        //Limpiar datos (informacion introducida por el usuario)
        txtNombre.setText("");
        txtApellido.setText("");
        txtCorreo.setText("");
        txtCelular.setText("");

    }//GEN-LAST:event_btnGuardarActionPerformed

